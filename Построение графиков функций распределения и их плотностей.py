plt.ylabel('y')
plt.title('Нормальное распределение')
plt.legend()
plt.grid()
plt.show()
y = norm.pdf(x, 1, 2)
plt.plot(x, y, color='red', label=f'N(m={1}, s={2})')
plt.xlabel('x')
plt.ylabel('y')
plt.title('Плотность нормального распределения')
plt.legend()
plt.grid()
plt.show()

x = np.linspace(-2, 10, 100)
y = expon.cdf(x, 1)
